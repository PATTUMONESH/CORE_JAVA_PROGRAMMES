package operartors_programes;

public class reltional_operators {

	public static void main(String[] args) {
		int a=1;
		int b=10;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>=b);    //(10>=10) || (a==10)
		System.out.println(a<=b);
	}

}
