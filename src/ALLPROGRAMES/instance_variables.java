package ALLPROGRAMES;
class ABC
{ //INSTANCE VARIABLES
	int a=10;
	int b;
	int c;
}

public class instance_variables {


		public static void main(String[] args) {
			 ABC obj=new ABC();
			obj.b=20;
			obj.c=30;
			   System.out.println(obj.a);
			   System.out.println(obj.b);
			   System.out.println(obj.c);
	}

}
